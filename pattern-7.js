let row = 8, k=1;

for (let i = 0; i < row*2-1; i++) {
    let line = '';
    for (let j = 0; j < row; j++) {
        if (i < row && j<=i || i >= row && j < row-k) {
            line += "* ";
        } else {
            line += "  ";
        }
    }
    if (i >= row) {
        k++;
    }
    console.log(line);
}